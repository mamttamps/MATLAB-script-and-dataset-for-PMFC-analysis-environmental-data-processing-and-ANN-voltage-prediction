%% train_pmfc_ann_fitnet_10min.m
% Sathish Kumar Kamaraj, PhD
% Wilgince Apollo, PhD
% Marco Antonio Merino Treviño. PhD
% Jazmín del Carmen Rojas Sánchez, MSc
% One-step-ahead (10 min) PMFC voltage forecasting with an MLP ANN (fitnet).
% Multi-output regression with 6 outputs (PMFC1-V ... PMFC6-V).
%
% Inputs:
%   - Exogenous variables: per-cell temperature + ambient temperature/RH.
%   - Autoregressive terms: voltage history through lags.
%
% Outputs (files):
%   - pmfc_fitnet_model_10min.mat
%   - pmfc_results_final.mat
%   - PMFC_2x3_tracking.png
%   - PMFC{i}_timeseries_residual.png
%   - PMFC{i}_regression.png

clear; clc; close all;

%% 0) Parameters
file = 'ALL_clean_fixed_qc_10_45.csv';

dt_min = 10;            % sampling interval (min)
horizon_steps = 1;      % 1 step = 10 min ahead

lagV = 3;               % V(t), V(t-1), V(t-2)
lagX = 1;               % X(t), X(t-1)

pTrain = 0.70;
pVal   = 0.15;
pTest  = 0.15;

hidden   = [20 10];     % hidden layer sizes
trainFcn = 'trainlm';   % Levenberg–Marquardt
max_fail = 8;
epochs   = 500;

normMin = 0;            % mapminmax lower bound
normMax = 1;            % mapminmax upper bound

export_dpi = 300;       % resolution for exported figures

%% 1) Load data table
T = readtable(file, "VariableNamingRule","preserve");

Vcols = ["PMFC1-V","PMFC2-V","PMFC3-V","PMFC4-V","PMFC5-V","PMFC6-V"];
Tcols = ["PMFC1-Temp","PMFC2-Temp","PMFC3-Temp","PMFC4-Temp","PMFC5-Temp","PMFC6-Temp"];
Acols = ["Temp-Amb","Hum-Amb"];

allCols = [Vcols, Tcols, Acols];
missing = setdiff(allCols, string(T.Properties.VariableNames));
if ~isempty(missing)
    error("Missing columns in CSV file: %s", strjoin(missing, ", "));
end

%% 2) Base matrices (exogenous inputs and outputs)
X0 = [T{:,Tcols}, T{:,Acols}];   % (N x 8)  -> cell temperatures + ambient T/RH
Y0 =  T{:,Vcols};                % (N x 6)  -> PMFC voltages

%% 3) Build lagged matrices
% Exogenous lags
X_lagged = [];
for k = 0:lagX
    X_lagged = [X_lagged, lagmatrix_simple(X0, k)];
end

% Voltage lags (autoregressive part)
V_lagged = [];
for k = 0:(lagV-1)
    V_lagged = [V_lagged, lagmatrix_simple(Y0, k)];
end

% Full feature set
Xall = [X_lagged, V_lagged];
fprintf('Features per sample: %d\n', size(Xall,2));

%% 4) Target at t + horizon_steps
Ytarget = lagmatrix_simple(Y0, -horizon_steps);

% Remove rows with NaNs generated by lagging
valid = all(~isnan(Xall),2) & all(~isnan(Ytarget),2);
Xall = Xall(valid,:);
Ytarget = Ytarget(valid,:);

fprintf('Valid samples after lagging/cleaning: %d\n', size(Xall,1));

%% 5) Temporal split: Train / Val / Test (by index order)
N = size(Xall,1);
nTrain = floor(pTrain*N);
nVal   = floor((pTrain+pVal)*N) - nTrain;

idxTrain = 1 : nTrain;
idxVal   = (nTrain+1) : (nTrain+nVal);
idxTest  = (nTrain+nVal+1) : N;

Xtrain = Xall(idxTrain,:); Ytrain = Ytarget(idxTrain,:);
Xval   = Xall(idxVal,:);   Yval   = Ytarget(idxVal,:);
Xtest  = Xall(idxTest,:);  Ytest  = Ytarget(idxTest,:);

fprintf('Train: %d | Val: %d | Test: %d\n', size(Xtrain,1), size(Xval,1), size(Xtest,1));

%% 6) Normalization (mapminmax) using TRAIN stats
[XtrainN, psX] = mapminmax(Xtrain', normMin, normMax);
[YtrainN, psY] = mapminmax(Ytrain', normMin, normMax);

XvalN  = mapminmax('apply', Xval', psX);
XtestN = mapminmax('apply', Xtest', psX);

YvalN  = mapminmax('apply', Yval', psY);
YtestN = mapminmax('apply', Ytest', psY);

%% 7) Network definition and training
net = fitnet(hidden, trainFcn);
net.divideFcn = 'divideind';   % we manually define indices

% Concatenate normalized sets for MATLAB's train()
XallN = [XtrainN, XvalN, XtestN];
YallN = [YtrainN, YvalN, YtestN];

nTrainS = size(XtrainN,2);
nValS   = size(XvalN,2);
nTestS  = size(XtestN,2);

net.divideParam.trainInd = 1:nTrainS;
net.divideParam.valInd   = (nTrainS+1):(nTrainS+nValS);
net.divideParam.testInd  = (nTrainS+nValS+1):(nTrainS+nValS+nTestS);

net.performFcn = 'mse';
net.trainParam.max_fail = max_fail;
net.trainParam.epochs   = epochs;

[net, tr] = train(net, XallN, YallN);

%% 8) Prediction on TEST set and denormalization
YhatN = net(XtestN);                         % (6 x samples)
Yhat  = mapminmax('reverse', YhatN, psY)';   % (samples x 6)

t_min = (0:size(Ytest,1)-1)' * dt_min;       % time axis in minutes

%% 9) Performance metrics (per PMFC)
metrics = table('Size',[6 6], ...
    'VariableTypes',{'double','double','double','double','double','double'}, ...
    'VariableNames',{'RMSE','MAE','R2_SSE','R2_corr','R','N'}, ...
    'RowNames',cellstr(Vcols));

for i = 1:6
    y    = Ytest(:,i);
    yhat = Yhat(:,i);

    rmse = sqrt(mean((y-yhat).^2));
    mae  = mean(abs(y-yhat));

    ssres = sum((y-yhat).^2);
    sstot = sum((y-mean(y)).^2);
    r2_sse = 1 - ssres/sstot;

    R = corr(y, yhat, 'Rows','complete');
    r2_corr = R^2;

    metrics{i,'RMSE'}    = rmse;
    metrics{i,'MAE'}     = mae;
    metrics{i,'R2_SSE'}  = r2_sse;
    metrics{i,'R2_corr'} = r2_corr;
    metrics{i,'R'}       = R;
    metrics{i,'N'}       = numel(y);
end

disp(metrics);

%% 10) Global 2×3 mosaic figure (voltage + error for all PMFCs)
figure('Color','w');

for iCell = 1:6
    if iCell <= 3
        rowBase = 0;
        col = iCell;
    else
        rowBase = 2;
        col = iCell - 3;
    end

    idxV = (rowBase + 1 - 1)*3 + col; % row for voltage
    idxE = (rowBase + 2 - 1)*3 + col; % row for error

    y_meas = Ytest(:,iCell);
    y_pred = Yhat(:,iCell);
    eV = y_meas - y_pred;

    % Voltage subplot
    subplot(4,3,idxV);
    plot(t_min, y_meas, 'b-', 'LineWidth', 1.0); hold on;
    plot(t_min, y_pred, 'r--', 'LineWidth', 1.0);
    grid on;
    title(sprintf('PMFC%d', iCell));
    ylabel('V (V)');

    % Error subplot
    subplot(4,3,idxE);
    plot(t_min, eV, 'k-', 'LineWidth', 0.9);
    grid on;
    xlabel('Time (min)');
    ylabel('Error (V)');
end

% One legend for all traces (using NaN placeholders)
subplot(4,3,1); hold on;
h1 = plot(nan,nan,'b-','LineWidth',1.0);
h2 = plot(nan,nan,'r--','LineWidth',1.0);
h3 = plot(nan,nan,'k-','LineWidth',0.9);
legend([h1 h2 h3], {'Measured','ANN','Error'}, 'Location','bestoutside');

exportgraphics(gcf, 'PMFC_2x3_tracking.png', 'Resolution', export_dpi);

%% 11) Per-cell figures (time series + residuals) and regression plots
for iCell = 1:6
    y_meas = Ytest(:, iCell);
    y_pred = Yhat(:,  iCell);

    R_val  = corr(y_meas, y_pred, 'Rows','complete');
    R2_val = R_val^2;
    rmse_val = sqrt(mean((y_meas - y_pred).^2, 'omitnan'));

    fprintf('PMFC%d -> R^2(corr): %.4f | RMSE: %.4f V\n', iCell, R2_val, rmse_val);

    % Time series + residuals
    fig1 = figure('Name', sprintf('PMFC%d_timeseries_residual', iCell), 'Color', 'w');
    subplot(2,1,1);
    plot(t_min, y_meas, 'b-', 'LineWidth', 1.5); hold on;
    plot(t_min, y_pred, 'r--', 'LineWidth', 1.5);
    grid on;
    ylabel('Voltage (V)');
    title(sprintf('PMFC %d: Measured vs ANN Prediction', iCell));
    legend('Measured Data','ANN Prediction','Location','best');

    subplot(2,1,2);
    eV = y_meas - y_pred;
    plot(t_min, eV, 'k-', 'LineWidth', 1.0);
    grid on;
    xlabel('Time (min)');
    ylabel('Error (V)');
    title('Residual (Measured - ANN)');

    exportgraphics(fig1, sprintf('PMFC%d_timeseries_residual.png', iCell), 'Resolution', export_dpi);

    % Regression plot
    fig2 = figure('Name', sprintf('PMFC%d_regression', iCell), 'Color', 'w');
    plotregression(y_meas', y_pred', sprintf('PMFC %d Regression', iCell));
    exportgraphics(fig2, sprintf('PMFC%d_regression.png', iCell), 'Resolution', export_dpi);

    % Update metrics with correlation-based R2/RMSE (for consistency)
    metrics{iCell,'R2_corr'} = R2_val;
    metrics{iCell,'RMSE'}    = rmse_val;
    metrics{iCell,'R'}       = R_val;
end

%% 12) Save model and results
save('pmfc_fitnet_model_10min.mat', ...
     'net','psX','psY','lagV','lagX','Vcols','Tcols','Acols','metrics','tr', ...
     'dt_min','horizon_steps','hidden','trainFcn','max_fail','epochs','pTrain','pVal','pTest');

save('pmfc_results_final.mat', 'net', 'metrics');
disp('Training and evaluation completed.');

%% ===== Helper function =====
function M = lagmatrix_simple(X, k)
    % Simple lag/lead operation for matrix X.
    % k > 0 : lag by k samples (shift down, leading rows = NaN)
    % k = 0 : original matrix
    % k < 0 : lead by |k| samples (shift up, trailing rows = NaN)

    [n, m] = size(X);
    M = nan(n, m);

    if k >= 0
        M((k+1):end,:) = X(1:(end-k),:);
    else
        kk = abs(k);
        M(1:(end-kk),:) = X((kk+1):end,:);
    end
end